function buildCatalogBlock(): string {
  return categories
    .map((c) => {
      const subs = c.subcategories.map((s) => s.name).join(", ");
      return `- ${c.name}: ${c.description}. Разделы: ${subs}.`;
    })
    .join("\n");
}

function buildPricingBlock(): string {
  const types = PRODUCT_TYPES.map(
    (p) => `  • ${p.label} — от ${formatRub(p.basePrice)} (${p.description})`,
  ).join("\n");
  const sizes = SIZES.map(
    (s) => `  • ${s.label}${s.hint ? ` (${s.hint})` : ""} — ×${s.value}`,
  ).join("\n");
  const colors = COLORS.map(
    (c) => `  • ${c.label} — +${Math.round(c.value * 100)}%`,
  ).join("\n");
  const complexity = COMPLEXITIES.map(
    (c) => `  • ${c.label} — ×${c.value}`,
  ).join("\n");

  return [
    "Базовая стоимость изделия среднего размера:",
    types,
    "Множитель за размер:",
    sizes,
    "Надбавка за окрашивание:",
    colors,
    "Множитель за сложность плетения:",
    complexity,
    "Формула: базовая цена × размер × (1 + окрашивание) × сложность.",
    "Итоговую цену называй ориентировочным диапазоном (±10–15%) и уточняй, что точную стоимость подтверждает мастер.",
  ].join("\n");
}

function buildWorkshopsBlock(): string {
  return workshops
    .map(
      (w) =>
        `  • «${w.title}» — ${categoryLabels[w.category]}, уровень «${levelLabels[w.level]}», ${formatRub(w.price)}. ${w.lessonsCount} уроков, ${w.duration}.`,
    )
    .join("\n");
}

